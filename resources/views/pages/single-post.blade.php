                <h2>면접관을 위한 면접 진행 Tip</h2>
                <p>
                    1.	다음 단계의 질문하기
                    예측 가능하고 구조화된 질문에 의존하기 보다 다음 단계의 생각이 필요한 답변을 들었을 때,
                    지원자에게 특히 적합한 인터뷰 질문을 할 수 있습니다. 실제 사람을 보고있다는 만족감이 들때까지
                    들은 내용을 명확하게 하여 단순한 질문 및 답변이 아닌 진정한 대화형 대화를 나누는 시간을 갖도록
                    합니다. <br />

                    예를들면 아래와 같습니다.
                    *돌이켜보면 다르게 행동할 걸 생각이 드는 일이 있나요?
                    *상황을 고려해서 최선을 다했다고 생각하십니까?
                    *오늘날 비슷한 상황이 생긴다면 어떻게 대처하시겠습니까?
                    <br />
                    2.	준비된 답변을 넘어 질문하기
                    의도치 않게 지원자가 원하는 대답을 하게 하는 질문은 피하는 것이 좋습니다. 이런 유형의 질문은
                    지원자가 준비한 답변을 하여 더 쉽게 응답하게 합니다. 예를 들어 지원자에게 ‘회사가 변화하고 있던
                    시기와 그것이 업무에 어떤 영향을 미쳤는지 설명해주세요.’라고 한다면 ‘어떻게 적응했습니까?’ 하고
                    묻지 않습니다. 지원자들은 ‘그 순간에 적응하여 가치 있는 것을 배웠습니다.’등의 준비된 답변을 하게
                    될 것입니다. 면접에서는 자연스럽게 진행하는 대화는 포장된 답변이 아닌 지원자의 솔직한 역량을
                    이끌어낼 수 있게됩니다.
                    <br />
                    3.	반복되는 증거 찾기
                    지원자들이 어떤 기회가 있었고, 어떤 도전을 하며 역경을 극복했는지에 대해 듣는다면 일반적으로
                    지원자들이 어떻게 행동해왔는지 알 수 있습니다. 하지만 이것은 전체적인 이야기가 아니라 부분적으로
                    준비된 답변 일 수도 있습니다.
                    <br />
                    예를들면 아래와 같습니다.
                    *프로젝트에 다른 접근방식이 필요하고 궁극적으로 성공했다고 생각했던 때를 알려주세요.
                    *팀 프로젝트의 대부분을 맞아 부담이 되었지만 결국, 팀의 성공에 기여했던 사례를 알려주세요.
                    *반대의 의견을 가진 사람과 함께 일해야 했지만 좋은 결과를 얻을 수 있었던 사례를 알려주세요.
                    <br />
                    4.	지원자가 과거 경험에서 무엇을 배웠는지 물어보기
                    모든 사람은 경력의 다양한 시점에서 직업이나 업무를 처음 접하는 사람입니다. 즉, 실수를 하고 더 나은
                    작업 방식을 깨닫고 자신의 경험에서 교훈을 얻었는지 확인해 보는 것이 좋습니다. 지원자들이 과거
                    경험에서 배운 것에 대한 질문을 하는 것은 채용중인 포지션에서 앞으로도 성장할 수 있는 능력을
                    보여주고 그들이 주장한 성취의 진정성을 확인하는데 도움이 됩니다.
                    <br />
                    지원자들은 면접 공간에서 긴장하거나 자신감을 잃을 수 있으므로 자신이 경험을 통해 어떻게 성장했는지
                    이야기 하고 설명하는데 편안한 감정을 느낄 수 있도록 최선을 다해야 합니다.
